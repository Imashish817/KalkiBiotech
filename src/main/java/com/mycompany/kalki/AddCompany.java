    private void Savetheshit(){
    if (!jTextField1.getText().isEmpty() || !jTextField2.getText().isEmpty() || !jTextField4.getText().isEmpty()) {
            MongoDBCalls DBCalls = new MongoDBCalls();
            boolean b = DBCalls.insertSeller(jTextField1.getText(), jTextField2.getText(), jTextField3.getText(), jTextField4.getText(), jComboBox1.getSelectedItem().toString());
            if (b) {
                JOptionPane.showMessageDialog(rootPane, "Seller Added Successfully");
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Somthing Went Worng");
            }

        }
        else{
        JOptionPane.showMessageDialog(rootPane, "All Details are Required..");
        }
    }
